
export const getBgColor = (variant) => {
    switch (variant) {
      case "id-1":
        return "rgb(212, 152, 237)";
      case "id-2":
        return "rgb(147, 33, 234)";
      case "id-3":
        return "rgb(228, 92, 180)";
      case "id-4":
        return "rgb(109, 40, 73)";
      case "id-5":
        return "rgb(238, 81, 157)";
      default:
    }
  };